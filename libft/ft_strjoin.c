#include "libft.h"

char	*ft_strjoin(char const *s1, char const *s2)
{
	size_t	str_len;
	char	*str;

	str_len = ft_strlen(s1) + ft_strlen(s2) + 1;
	str = malloc(str_len);
	if (!str)
		return (0);
	ft_strlcpy(str, s1, ft_strlen(s1) + 1);
	ft_strlcat(str, s2, str_len);
	return (str);
}
